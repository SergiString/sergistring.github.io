    function applyLanguage(lang) {
      currentLang = lang;
      document.documentElement.lang = lang;
      const dict = translations[lang] || translations.ca;
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (dict[key]) el.textContent = dict[key];
      });
      document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (dict[key]) el.setAttribute('placeholder', dict[key]);
      });
      labelEls.modalLabel.textContent = dict.modal_label;
      labelEls.role.textContent = dict.role_label;
      labelEls.goal.textContent = dict.goal_label;
      labelEls.challenge.textContent = dict.challenge_label;
      labelEls.learned.textContent = dict.learned_label;
      labelEls.next.textContent = dict.next_label;
      langSwitcher.value = lang;
      localStorage.setItem('portfolio-language', lang);
    }

    function applyTheme(theme) {
      currentTheme = theme;
      body.setAttribute('data-theme', theme);
      localStorage.setItem('portfolio-theme', theme);
    }

    function makeLinkButton(type, href) {
      const dict = translations[currentLang];
      const a = document.createElement(type === 'private' ? 'span' : 'a');
      a.className = 'btn btn-outline';
      if (type === 'github') {
        a.href = href;
        a.target = '_blank';
        a.rel = 'noopener';
        a.textContent = dict.action_github;
      } else if (type === 'githubProfile') {
        a.href = href;
        a.target = '_blank';
        a.rel = 'noopener';
        a.textContent = dict.action_profile;
      } else if (type === 'demo') {
        a.href = href;
        a.target = '_blank';
        a.rel = 'noopener';
        a.textContent = dict.action_demo;
      } else {
        a.textContent = dict.action_private;
        a.style.opacity = '.72';
        a.style.cursor = 'default';
      }
      return a;
    }

    function openProject(key) {
      const project = projectData[key];
      if (!project) return;
      modalTitle.textContent = project.title;
      modalIntro.textContent = project.intro[currentLang];
      modalRole.textContent = project.role[currentLang];
      modalGoal.textContent = project.goal[currentLang];
      modalChallenge.textContent = project.challenge[currentLang];
      modalNext.textContent = project.next[currentLang];

      modalLearned.innerHTML = '';
      project.learned[currentLang].forEach(item => {
        const li = document.createElement('li');
        li.textContent = item;
        modalLearned.appendChild(li);
      });

      modalTech.innerHTML = '';
      project.tech.forEach(item => {
        const span = document.createElement('span');
        span.className = 'tech-pill';
        span.textContent = item;
        modalTech.appendChild(span);
      });

      modalLinks.innerHTML = '';
      (project.links || []).forEach(link => {
        modalLinks.appendChild(makeLinkButton(link.type, link.href));
      });

      if (project.previewType === 'image') {
        modalPreview.innerHTML = `<img src="${project.previewSrc}" alt="${project.title} preview" />`;
      } else {
        modalPreview.innerHTML = mockPreview(project.mockLabel || project.title);
      }

      modal.classList.add('open');
      modal.setAttribute('aria-hidden', 'false');
      body.style.overflow = 'hidden';
    }

